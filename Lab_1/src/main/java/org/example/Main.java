package org.example;
public class Main {
        public static void main(String[] args) {
                Животное[] животные = {
                        new Собака("Бобик"),
                        new Кот("Пушок"),
                        new Тигр("Шерхан"),
                        new Хомяк("Хома"),
                        new Корова("Зойка"),
                        new Коза("Дуся"),
                        new Тираннозавр("Рекс", 8000),
                        new Диплодок("Диппи", 12000),
                        new Стегозавр("Стег", 2000)
                };

                // Тестируем действия животных
                for (Животное животное : животные) {
                        животное.run(150);
                        животное.swim(5);
                }

                // Выводим количество созданных экземпляров
                System.out.println("Количество созданных животных: " + Животное.getCount());

                // Выводим вес динозавров
                if (животные[6] instanceof Динозавр) {
                        ((Динозавр) животные[6]).weight();
                }
                if (животные[7] instanceof Динозавр) {
                        ((Динозавр) животные[7]).weight();
                }
                if (животные[8] instanceof Динозавр) {
                        ((Динозавр) животные[8]).weight();
                }

                // Выводим голоса домашних животных
                for (Животное животное : животные) {
                        if (животное instanceof ДомашниеЖивотные) {
                                ((ДомашниеЖивотные) животное).voice();
                        }
                }
        }
}